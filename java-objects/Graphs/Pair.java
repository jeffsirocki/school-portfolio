/**
 * 
 * @author Jeff Sirocki, Marco Duran
 * The Pair class contains pairs of strings. 
 * The pair contains the from city and the to city.
 *
 */
class Pair{
	/**
	* city name from.
	*/
	String from;
	/**
	* city name to.
	*/
	String to;
	
	/**
	* Default constructor for Pair
	* makes a pair of strings
	*/
	Pair(String from, String to){
		this.from = from;
		this.to = to;
	}
}